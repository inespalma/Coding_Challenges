//<---String Concatenation--->

let a = "Ol치";
let b = "Mundo";

// Using the concat() method
let c = a.concat(" ", b);

// Using template literals
let d = `${a} ${b}`;

// Using the + operator
let e = "Ol치" + " Mundo";

// Using the + operator with line breaks
let f = "Ol치" + "\n" + " Mundo";

console.log(c);
console.log(d);
console.log(e);
console.log(f);

